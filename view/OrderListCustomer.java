    public void customerListTable() throws FileNotFoundException, IOException{
       String[] columnName = new String[7];
       columnName[0] = "Buyer Id";
       columnName[1] = "Consumer Name";
       columnName[2] = "Product Name";
       columnName[3] = "Category";
       columnName[4] = "Quantity";
       columnName[5] = "Total Amount";
       columnName[6] = "Date";
       
        tblCustomerList.setRowHeight(30);
        File file = new File("src/textFiles/Order.txt");
        File f = new File("src/textFiles/activeUser.txt");
        BufferedReader bru = new BufferedReader(new FileReader(f));
        Object[] tableLinesUser = bru.lines().toArray();
        String lineUser = tableLinesUser[0].toString().trim();
        String[] UserRow = lineUser.split(",");
        BufferedReader br = new BufferedReader(new FileReader(file));
        DefaultTableModel model = (DefaultTableModel) tblCustomerList.getModel();
        model.setColumnIdentifiers(columnName);
       Object[] tableLines = br.lines().toArray();
       int i;
       
       for(i=0; i<tableLines.length;i++){
           String line = tableLines[i].toString().trim();
           String[] tableRow = line.split(",");
           if(tableRow[0].equals(UserRow[0]) && tableRow[1].equals(UserRow[1])){
            model.addRow(tableRow);
           }
       }
    }

